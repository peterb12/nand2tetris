module HackCode where
import HackParse

encodeDest :: Maybe Dest -> String
encodeDest Nothing    = "000"
encodeDest (Just M)   = "001"
encodeDest (Just D)   = "010"
encodeDest (Just MD)  = "011"
encodeDest (Just A)   = "100"
encodeDest (Just AM)  = "101"
encodeDest (Just AD)  = "110"
encodeDest (Just AMD) = "111"

encodeJump :: Maybe Jump -> String
encodeJump Nothing    = "000"
encodeJump (Just JGT) = "001"
encodeJump (Just JEQ) = "010"
encodeJump (Just JGE) = "011"
encodeJump (Just JLT) = "100"
encodeJump (Just JNE) = "101"
encodeJump (Just JMP) = "111"

encodeComp :: Comp -> String
encodeComp Zero      = "101010"
encodeComp One       = "111111"
encodeComp NegOne    = "111010"
encodeComp Dee       = "001100"
encodeComp Ay        = "110000"
encodeComp Mm        = "110000"
encodeComp NotD      = "001101"
encodeComp NotA      = "110001"
encodeComp NotM      = "110001"
encodeComp NegD      = "001111"
encodeComp NegA      = "110011"
encodeComp NegM      = "110011"
encodeComp DPlusOne  = "011111"
encodeComp APlusOne  = "110111"
encodeComp MPlusOne  = "110111"
encodeComp DMinusOne = "001110"
encodeComp AMinusOne = "110010"
encodeComp MMinusOne = "110010"
encodeComp DPlusA    = "000010"
encodeComp DPlusM    = "000010"
encodeComp DMinusA   = "010011"
encodeComp DMinusM   = "010011"
encodeComp AMinusD   = "000111"
encodeComp MMinusD   = "000111"
encodeComp DAndA     = "000000"
encodeComp DAndM     = "000000"
encodeComp DOrA      = "010101"
encodeComp DOrM      = "010101"